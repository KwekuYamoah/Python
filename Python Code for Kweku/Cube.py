class Cube:
    def __init__(self, r):
        self.radius = radius
        self.surfaceArea = 0
        self.volume = 0
    
    def getRadius(self):
        r = self.radius
        self.area = 6 * r * r
        return (self.area)
    
    def getVolume(self):
        r = self.radius
        self.volume = r * r * r
        return (self.volume)
    
    
    